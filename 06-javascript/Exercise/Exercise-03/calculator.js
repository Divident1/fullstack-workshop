// Exercise 1.1: Basic Calculations
console.log('--- Exercise 1.1: Basic Calculations ---');

let a = 15;
let b = 4;

// 1. Sum of a and b
console.log('Sum (a + b):', a + b);

// 2. Difference (a - b)
console.log('Difference (a - b):', a - b);

// 3. Product (a * b)
console.log('Product (a * b):', a * b);

// 4. Division (a / b)
console.log('Division (a / b):', a / b);

// 5. Remainder (a % b)
console.log('Remainder (a % b):', a % b);

// 6. a raised to the power of b
console.log('Power (a ** b):', a ** b); // or Math.pow(a, b)

// Bonus: Explain why 15 % 4 = 3
console.log('Bonus Explanation: 15 divided by 4 is 3 with a remainder of 3. (4 * 3 = 12, 15 - 12 = 3)');
